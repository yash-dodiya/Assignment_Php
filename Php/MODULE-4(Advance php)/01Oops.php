<!--  What Is Object Oriented Programming? - -->


    <h2>Oop stands for Object-Oriented-Programming</h2>

    <p>Procedural programming is about writing procedures or functions that perform operations on the data, while object-oriented programming 
        is about creating objects that contain both data and functions</p>

    <h3>Object-oriented programming has several advantages over procedural programming:</h3>

    <ul type="squre">
        <li>OOP is faster and easier to execute</li>
        <li>OOP provides a clear structure for the programs</li>
        <li>OOP helps to keep the PHP code DRY "Don't Repeat Yourself", and makes the code easier to maintain, modify and debug</li>
        <li>OOP makes it possible to create full reusable applications with less code and shorter development time</li>
